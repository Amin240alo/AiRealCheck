// ─── Exports ──────────────────────────────────────────────────────────────────

export type Translation = typeof de;

export const translations: Record<Locale, Translation> = { de, en, fr, es };

/**
 * Resolve a dot-path key (e.g. 'auth.login.title') against a translation dict.
 * Falls back to the German dictionary if the key is missing in the target language.
 */
export function resolveTKey(lang: Locale, key: string): string {
  const dict = translations[lang] ?? translations[DEFAULT_LANG];
  const fallback = translations[DEFAULT_LANG];
  const parts = key.split('.');

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  let val: any = dict;
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  let fb: any = fallback;

  for (const p of parts) {
    val = val?.[p];
    fb = fb?.[p];
  }

  if (typeof val === 'string') return val;
  if (typeof fb === 'string') return fb;
  return key; // last resort
}
